TRAY_SCRIPT = """#!/usr/bin/env python3
import subprocess, os, sys

TRAY_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "Tray")
DELETE_DELAY = 240

def bash_delete_and_close(dst_file, tray_dir):
    script = f\"\"\"
    sleep {DELETE_DELAY}
    echo "Removing file: {dst_file}" >&2
    rm "{dst_file}"
    count=$(find "{tray_dir}" -type f -not -name ".*" | wc -l)
    echo "File count after delete: $count" >&2
    if [ "$count" -eq 0 ]; then
        osascript -e 'tell application "Finder"
            set winList to every window
            repeat with w in winList
                try
                    set p to POSIX path of (target of w as alias)
                    if p contains "{tray_dir}" then
                        close w
                    end if
                end try
            end repeat
        end tell'
    fi
    \"\"\"
    subprocess.Popen(['bash', '-c', script])

def get_screen_resolution():
    script = '''
    tell application "Finder"
        set screenSize to bounds of window of desktop
        set {x1, y1, x2, y2} to screenSize
        set screenWidth to x2 - x1
        set screenHeight to y2 - y1
        return (screenWidth as string) & "," & (screenHeight as string)
    end tell
    '''
    try:
        out = subprocess.check_output(['osascript', '-e', script])
        width, height = map(int, out.decode().strip().split(','))
        return width, height
    except:
        print("Failed to get logical screen size, falling back to default.")
        return 1440, 900

def show_tray():
    w, h = get_screen_resolution()
    left = 4 * w // 5
    top = 0
    right = w
    bottom = h // 5
    script = f'''
    tell application "Finder"
        set targetFolder to POSIX file "{TRAY_DIR}"
        open targetFolder
        tell front window
            set current view to icon view
            set bounds to {{{left}, {top}, {right}, {bottom}}}
            set toolbar visible to false
        end tell
    end tell
    '''
    subprocess.run(['osascript', '-e', script])

def copy_and_delete(filepath):
    os.makedirs(TRAY_DIR, exist_ok=True)
    filename = os.path.basename(filepath)
    dst = os.path.join(TRAY_DIR, filename)
    if os.path.exists(dst):
        if os.stat(dst).st_ino == os.stat(filepath).st_ino:
            bash_delete_and_close(dst, TRAY_DIR)
            return
        else:
            base, ext = os.path.splitext(dst)
            i = 1
            while os.path.exists(new := f"{base}_{i}{ext}"):
                i += 1
            dst = new
    os.link(filepath, dst)
    bash_delete_and_close(dst, TRAY_DIR)

if __name__ == "__main__":
    if len(sys.argv) == 2:
        copy_and_delete(sys.argv[1])
        show_tray()
"""

